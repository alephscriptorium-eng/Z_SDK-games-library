#!/usr/bin/env node
/**
 * Generate city mock volumes for firehose-browser (DISK_01) and cache-browser (DISK_02/LINEAS).
 *
 * Build-time only: reads a cantera CIUDAD tree passed via --cantera.
 * Runtime browsers never open the cantera; they consume --out (versioned volumes/).
 *
 * Idempotent: same cantera + censo → same bytes (sorted edges, fixed epoch timestamps).
 */

import fs from 'node:fs';
import fsp from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { createHash } from 'node:crypto';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PKG_ROOT = path.resolve(__dirname, '..');
const DEFAULT_OUT = path.join(PKG_ROOT, 'volumes');
const DEFAULT_CENSO = path.join(PKG_ROOT, 'data', 'censo.json');

/** Fixed epoch for deterministic createdAt (ISO). */
const EPOCH_MS = Date.parse('2026-07-01T00:00:00.000Z');

/** Blindness scrub: do not leave marco tokens in generated bytes. */
const CEGUERA = new RegExp(
  [
    'SCRIPT' + '_SDK',
    'S' + '_SDK',
    'HOL' + 'ONES',
    'hol[oó]n',
    'holarqu[ií]a',
    'jun' + 'tura'
  ].join('|'),
  'gi'
);

function parseArgs(argv) {
  const out = { cantera: null, out: DEFAULT_OUT, censo: DEFAULT_CENSO, dryRun: false };
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--cantera') out.cantera = argv[++i];
    else if (a === '--out') out.out = path.resolve(argv[++i]);
    else if (a === '--censo') out.censo = path.resolve(argv[++i]);
    else if (a === '--dry-run') out.dryRun = true;
    else if (a === '--help' || a === '-h') {
      console.log(`Usage: node tools/generate-ciudad-volumes.mjs --cantera <CIUDAD_DIR> [--out <dir>] [--censo <json>]`);
      process.exit(0);
    }
  }
  if (!out.cantera) {
    console.error('Missing --cantera <path to CIUDAD tree>');
    process.exit(2);
  }
  out.cantera = path.resolve(out.cantera);
  out.out = path.resolve(out.out);
  return out;
}

function scrub(text) {
  return String(text ?? '').replace(CEGUERA, '[redacted]');
}

function slug(s) {
  return String(s)
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-zA-Z0-9._-]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'x';
}

function readJson(p) {
  return JSON.parse(fs.readFileSync(p, 'utf8'));
}

function parseHandoffsTsv(tsvPath) {
  const raw = fs.readFileSync(tsvPath, 'utf8');
  const lines = raw.split(/\r?\n/).filter((l) => l.trim() && !l.startsWith('#'));
  const edges = [];
  for (const line of lines) {
    const cols = line.split('\t');
    if (cols.length < 3) continue;
    const [src, dst, ...rest] = cols;
    const label = rest.join('\t').trim();
    if (!src || !dst) continue;
    edges.push({
      src: src.trim(),
      dst: dst.trim(),
      label,
      barrioHint: null,
      graph: 'handoffs'
    });
  }
  return edges;
}

/** handoffs-barrios.tsv: scope path src dst label — scope is the barrio id. */
function parseBarriosTsv(tsvPath) {
  if (!fs.existsSync(tsvPath)) return [];
  const raw = fs.readFileSync(tsvPath, 'utf8');
  const lines = raw.split(/\r?\n/).filter((l) => l.trim() && !l.startsWith('#'));
  const edges = [];
  for (const line of lines) {
    const cols = line.split('\t');
    if (cols.length < 5) continue;
    const [scope, , src, dst, ...rest] = cols;
    const label = rest.join('\t').trim();
    if (!scope || !src || !dst) continue;
    edges.push({
      src: src.trim(),
      dst: dst.trim(),
      label,
      barrioHint: scope.trim(),
      graph: 'barrios'
    });
  }
  return edges;
}

/** handoffs-zigurat.tsv: src dst note */
function parseZiguratTsv(tsvPath) {
  if (!fs.existsSync(tsvPath)) return [];
  const raw = fs.readFileSync(tsvPath, 'utf8');
  const lines = raw.split(/\r?\n/).filter((l) => l.trim() && !l.startsWith('#'));
  const edges = [];
  for (const line of lines) {
    const cols = line.split('\t');
    if (cols.length < 2) continue;
    const [src, dst, ...rest] = cols;
    const label = rest.join('\t').trim() || 'zigurat';
    if (!src || !dst) continue;
    edges.push({
      src: src.trim(),
      dst: dst.trim(),
      label,
      barrioHint: null,
      graph: 'zigurat'
    });
  }
  return edges;
}

function loadAllEdges(cantera) {
  const grafo = path.join(cantera, 'GRAFO');
  const edges = [
    ...parseHandoffsTsv(path.join(grafo, 'handoffs.tsv')),
    ...parseBarriosTsv(path.join(grafo, 'handoffs-barrios.tsv')),
    ...parseZiguratTsv(path.join(grafo, 'handoffs-zigurat.tsv'))
  ];
  edges.sort((a, b) => {
    const ka = `${a.graph}\t${a.barrioHint || ''}\t${a.src}\t${a.dst}\t${a.label}`;
    const kb = `${b.graph}\t${b.barrioHint || ''}\t${b.src}\t${b.dst}\t${b.label}`;
    return ka.localeCompare(kb, 'en');
  });
  return edges;
}

function resolveBarrioId(actor, edge, censo) {
  if (edge.barrioHint && censo.barrios[edge.barrioHint]) return edge.barrioHint;
  if (censo.barrios[actor]) return actor;
  const aliases = censo.actorAliases || {};
  if (aliases[actor]) return aliases[actor];
  const lower = actor.toLowerCase();
  for (const [id] of Object.entries(censo.barrios)) {
    if (id.toLowerCase() === lower) return id;
  }
  for (const [alias, barrioId] of Object.entries(aliases)) {
    if (alias.toLowerCase() === lower) return barrioId;
  }
  // dst may be a barrio (zigurat edges)
  if (edge.dst && censo.barrios[edge.dst]) return edge.dst;
  return null;
}

function resolveEstado(actor, edge, censo) {
  const barrioId = resolveBarrioId(actor, edge, censo);
  if (barrioId && censo.barrios[barrioId]) return censo.barrios[barrioId].estado;
  // Plaza / Zigurat actors without barrio mapping: pulse as vivo
  return 'vivo';
}

function shouldEmit(estado, cadence, edgeIndex) {
  const rule = cadence[estado] || cadence.vivo;
  const every = rule.emitEvery ?? 1;
  if (every <= 0) return { emit: false, corpus: null, intensity: 'none' };
  if (edgeIndex % every !== 0) return { emit: false, corpus: null, intensity: rule.intensity };
  return {
    emit: true,
    corpus: rule.corpus || 'candidate',
    intensity: rule.intensity || 'high'
  };
}

function edgeRkey(edge, index) {
  const h = createHash('sha1')
    .update(`${edge.src}|${edge.dst}|${edge.label}|${index}`)
    .digest('hex')
    .slice(0, 12);
  return `c${String(index).padStart(5, '0')}-${h}`;
}

function buildPost(edge, index, estado, intensity) {
  const rkey = edgeRkey(edge, index);
  const createdAt = new Date(EPOCH_MS + index * 60_000).toISOString();
  const did = `did:plc:ciudad-${slug(edge.src).toLowerCase().slice(0, 24)}`;
  const text = scrub(
    `[pulso:${estado}/${intensity}] ${edge.src} → ${edge.dst}: ${edge.label}`
  );
  return {
    did,
    kind: 'commit',
    handle: `${slug(edge.src).toLowerCase()}.ciudad.local`,
    uri: `at://${did}/app.bsky.feed.post/${rkey}`,
    commit: {
      collection: 'app.bsky.feed.post',
      rkey,
      cid: `bafy${rkey}`,
      record: {
        text,
        createdAt,
        facets: [
          { typ: 'ciudad.estado', value: estado },
          { typ: 'ciudad.intensity', value: intensity },
          { typ: 'ciudad.src', value: edge.src },
          { typ: 'ciudad.dst', value: edge.dst }
        ]
      }
    }
  };
}

async function rmrf(dir) {
  await fsp.rm(dir, { recursive: true, force: true });
}

async function writeFileSorted(abs, content) {
  await fsp.mkdir(path.dirname(abs), { recursive: true });
  await fsp.writeFile(abs, content, 'utf8');
}

function listBarrioFichas(cantera) {
  const dir = path.join(cantera, '01-BARRIOS');
  return fs
    .readdirSync(dir)
    .filter((f) => /^\d+-.*\.md$/.test(f))
    .sort((a, b) => a.localeCompare(b, 'en'));
}

function barrioIdFromFichaName(fname) {
  // 02-MCPGallery.md → MCPGallery
  return fname.replace(/^\d+-/, '').replace(/\.md$/, '');
}

async function generateFirehose(outRoot, edges, censo) {
  const firehoseRoot = path.join(outRoot, 'DISK_01', 'FIREHOSE');
  await rmrf(firehoseRoot);
  const corporaDirs = ['candidate', 'raw', 'discarded', 'labeled'];
  for (const c of corporaDirs) {
    await fsp.mkdir(path.join(firehoseRoot, c, 'pulso'), { recursive: true });
  }

  const counts = { candidate: 0, raw: 0, discarded: 0, labeled: 0 };
  const { cadence } = censo;
  let emitIndex = 0;

  for (let i = 0; i < edges.length; i++) {
    const edge = edges[i];
    const estado = resolveEstado(edge.src, edge, censo);
    const decision = shouldEmit(estado, cadence, i);
    if (!decision.emit) continue;
    const corpus = decision.corpus;
    const post = buildPost(edge, emitIndex, estado, decision.intensity);
    const fname = `${post.commit.rkey}.json`;
    const body = `${JSON.stringify(post, null, 2)}\n`;
    await writeFileSorted(path.join(firehoseRoot, corpus, 'pulso', fname), body);
    counts[corpus] = (counts[corpus] || 0) + 1;
    emitIndex += 1;
  }

  const triage = {
    version: 1,
    batches: ['pulso'],
    generated: 'ciudad-mockdatas',
    edgeCount: edges.length,
    postCount: emitIndex
  };
  await writeFileSorted(
    path.join(firehoseRoot, 'triage-manifest.json'),
    `${JSON.stringify(triage, null, 2)}\n`
  );

  return counts;
}

async function generateLineas(outRoot, cantera, censo) {
  const lineasRoot = path.join(outRoot, 'DISK_02', 'LINEAS');
  await rmrf(lineasRoot);
  const ciudadRoot = path.join(lineasRoot, 'ciudad');
  await fsp.mkdir(path.join(ciudadRoot, 'nodos', 'N01'), { recursive: true });
  await fsp.mkdir(path.join(ciudadRoot, 'distritos'), { recursive: true });

  const mapaSrc = path.join(cantera, 'MAPA.md');
  if (fs.existsSync(mapaSrc)) {
    await writeFileSorted(path.join(ciudadRoot, 'MAPA.md'), scrub(fs.readFileSync(mapaSrc, 'utf8')));
  }

  const capasSrc = path.join(cantera, '00-CAPAS.md');
  if (fs.existsSync(capasSrc)) {
    await writeFileSorted(path.join(ciudadRoot, '00-CAPAS.md'), scrub(fs.readFileSync(capasSrc, 'utf8')));
  }

  // Census as browsable metadata at city root
  await writeFileSorted(
    path.join(ciudadRoot, 'censo.json'),
    `${JSON.stringify(censo, null, 2)}\n`
  );

  const fichas = listBarrioFichas(cantera);
  for (const fname of fichas) {
    const barrioId = barrioIdFromFichaName(fname);
    const meta = censo.barrios[barrioId] || { estado: 'latente', distrito: 'Sin-distrito' };
    const distritoSlug = slug(meta.distrito);
    const barrioDir = path.join(ciudadRoot, 'distritos', distritoSlug, barrioId);
    await fsp.mkdir(barrioDir, { recursive: true });
    const fichaBody = scrub(fs.readFileSync(path.join(cantera, '01-BARRIOS', fname), 'utf8'));
    await writeFileSorted(path.join(barrioDir, 'ficha.md'), fichaBody);
    await writeFileSorted(
      path.join(barrioDir, 'meta.json'),
      `${JSON.stringify(
        {
          id: barrioId,
          estado: meta.estado,
          distrito: meta.distrito,
          ficha: 'ficha.md',
          sourceFicha: fname
        },
        null,
        2
      )}\n`
    );
  }

  // Minimal linea-kit manifest so cache-browser can list the line
  const manifest = {
    meta: {
      corpus: 'LINEAS/ciudad',
      version: '0.1.0',
      generated_at: '2026-07-01T00:00:00.000Z',
      source: 'ciudad-mockdatas',
      autor_tronco: 'mockdatas',
      referencia_wp_cima: 'Ciudad_Mapa',
      nodo_count: 1,
      partes: [
        {
          id: 'I',
          titulo: 'Ciudad',
          año_ini: 2024,
          año_fin: 2026,
          nodos: ['N01']
        }
      ]
    },
    nodos: [{ id: 'N01', paths: { meta: 'nodos/N01/meta.json' } }]
  };
  await writeFileSorted(path.join(ciudadRoot, 'manifest.json'), `${JSON.stringify(manifest, null, 2)}\n`);
  await writeFileSorted(
    path.join(ciudadRoot, 'nodos', 'N01', 'meta.json'),
    `${JSON.stringify(
      {
        id: 'N01',
        parte: 'I',
        año_ini: 2024,
        año_fin: 2026,
        etiqueta: 'Plaza / mapa',
        tesis: 'City as filesystem — distritos → barrios → ficha.md',
        articulos_wp: ['Ciudad_Mapa']
      },
      null,
      2
    )}\n`
  );

  const registry = [
    {
      id: 'ciudad',
      path: 'ciudad',
      etiqueta: 'Ciudad (mockdatas)',
      autor_tronco: 'mockdatas',
      nodo_prefix: 'N',
      nodo_count: 1,
      referencia_wp_cima: 'Ciudad_Mapa'
    }
  ];
  // yaml-ish without dependency: emit minimal YAML list
  const yamlLines = registry.flatMap((e) => [
    `- id: ${e.id}`,
    `  path: ${e.path}`,
    `  etiqueta: "${e.etiqueta}"`,
    `  autor_tronco: ${e.autor_tronco}`,
    `  nodo_prefix: ${e.nodo_prefix}`,
    `  nodo_count: ${e.nodo_count}`,
    `  referencia_wp_cima: ${e.referencia_wp_cima}`
  ]);
  await writeFileSorted(path.join(lineasRoot, 'registry.yaml'), `${yamlLines.join('\n')}\n`);

  return { barrioCount: fichas.length };
}

async function writeVolumesJson(outRoot, firehoseCounts) {
  const volumes = {
    root: '.',
    policy: 'mockdatas-ciudad',
    volumes: {
      firehose: {
        disk: 'DISK_01',
        path: 'DISK_01/FIREHOSE',
        readonly: true,
        label: 'Firehose — city pulse',
        corpora: [
          { id: 'candidate', path: 'candidate', label: 'Pulso (candidate)', files: firehoseCounts.candidate || 0 },
          { id: 'raw', path: 'raw', label: 'Raw', files: firehoseCounts.raw || 0 },
          { id: 'discarded', path: 'discarded', label: 'Discarded', files: firehoseCounts.discarded || 0 },
          { id: 'labeled', path: 'labeled', label: 'Labeled', files: firehoseCounts.labeled || 0 }
        ]
      },
      lineas: {
        disk: 'DISK_02',
        path: 'DISK_02/LINEAS',
        readonly: true,
        label: 'Lineas — city filesystem'
      }
    }
  };
  await writeFileSorted(path.join(outRoot, 'volumes.json'), `${JSON.stringify(volumes, null, 2)}\n`);
}

async function main() {
  const args = parseArgs(process.argv);
  if (!fs.existsSync(args.cantera)) {
    console.error(`Cantera not found: ${args.cantera}`);
    process.exit(2);
  }
  const handoffs = path.join(args.cantera, 'GRAFO', 'handoffs.tsv');
  if (!fs.existsSync(handoffs)) {
    console.error(`Missing GRAFO/handoffs.tsv under ${args.cantera}`);
    process.exit(2);
  }
  const censo = readJson(args.censo);
  const edges = loadAllEdges(args.cantera);

  if (args.dryRun) {
    console.log(JSON.stringify({ cantera: args.cantera, out: args.out, edges: edges.length }, null, 2));
    return;
  }

  await fsp.mkdir(args.out, { recursive: true });
  const firehoseCounts = await generateFirehose(args.out, edges, censo);
  const lineasMeta = await generateLineas(args.out, args.cantera, censo);
  await writeVolumesJson(args.out, firehoseCounts);

  console.log(
    JSON.stringify(
      {
        ok: true,
        out: args.out,
        edges: edges.length,
        firehose: firehoseCounts,
        lineas: lineasMeta
      },
      null,
      2
    )
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
